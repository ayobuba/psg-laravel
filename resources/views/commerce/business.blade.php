        <h4>Starting a Business in Plateau (Jos) – Nigeria</h4>
        
        <p>Below is a detailed summary of the bureaucratic and legal hurdles an entrepreneur must overcome in order to incorporate and register a new firm, along with their associated time and set-up costs. It examines the procedures, time and cost involved in launching a commercial or industrial firm with up to 50 employees and start-up capital of 10 times the economy's per-capita gross national income.</p>

