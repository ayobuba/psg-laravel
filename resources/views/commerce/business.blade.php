        <h4>PROCEDURE</h4>

        <ul>
            <li><strong>Reserve a unique company name at the Corporate Affairs Commission </strong></li>
        </ul>
        <p>
            The Corporate Affairs Commission online system allows for online search of unique company names upon the purchase of an e-payment card for a flat fee of NGN 500 from an accredited bank. Until now the system is not fully operational either because of power fluctuation or network failures, applicants may have to go to the CAC office to complete this procedure. The time to conduct the availability of name search, receive confirmation and reserve the company name is usually 5 days. The company’s name reservation lasts 60 days but can be renewed for a similar period.
        </p>

        <ul>
            <li><strong>Prepare the requisite incorporation documents and pay the stamp duty</strong></li>
        </ul>
        <p>
            This procedure involves the following tasks: 
            <ol type="a">
                <li>Preparing and printing the memorandum and articles of association;</li>
                <li>Completing the requisite statutory forms, and;</li>
                <li>Having the memorandum and articles of association stamped by the Federal Inland Revenue Service.</li>
            </ol>
        </p>
        <p>
            There is no stamp duty office in this state and the procedure is usually done in the nearest city where there is one. The time for preparation of documents and stamping presented here is based on an assumption that the stamping is conducted at the stamp duty office in Abuja and accounts for the travel cost.
        </p>

        <ul>
            <li><strong>Sign the declaration of compliance (Form CAC 4) before a Commissioner for Oaths or notary public</strong></li>
        </ul>

        <p>
            A declaration that the requirements of the Companies and Allied Matters Act have been met must be sworn by
            <ol type="i">
                <li>a barrister or solicitor engaged in the formation of the company or</li>
                <li>by a person named in the memorandum and articles of association as a director or secretary of the company. </li>
            </ol>


        </p>
        <p>This procedure can be done before a notary public or filed at the state or federal High Court.</p>
        <p>The common practice is for this declaration to be sworn before a Commissioner for Oaths at the state High Court.</p>

        <ul>
            <li>
                <strong>Obtain bank draft from a bank for payment of the registration fees </strong>
            </li>
        </ul>

        <p>Payment is made to any of the receiving banks designated by the Corporate Affairs Commission.</p>
        
        <ul>
            <li>
                <strong>Register the company at the Corporate Affairs Commission </strong>
            </li>
        </ul>

        <p>
            The applicant must file with the Corporate Affairs Commission the required incorporation documents and forms, which include: 
            <ol type="a">
                <li>Stamped memorandum and articles of association (2 copies);</li>
                <li>Form CAC 1: Availability check and reservation of name;</li>
                <li>Form CAC 2: Statement of share capital and return of allotment of shares;</li>
                <li>Form CAC 2.1: Particulars of the company secretary;</li>
                <li>Form CAC 3: Notice of registered address;</li>
                <li>Form CAC 4: Declaration of compliance;</li>
                <li>Form CAC 7: Particulars of directors;</li>
                <li>Photocopy of information page of international passport or national identity card for each director and subscriber. </li>
            </ol>
        </p>

        <p>The fee schedule for registration is the following: for the first NGN 1,000,000 of nominal capital: NGN 10,000; for every NGN 1,000,000 or less thereof of nominal capital after the first NGN 1,000,000: NGN 5,000. Fees for certified true copies of some forms are paid as follows: NGN 3,000 for C.T.C of memorandum and articles of association, NGN 2,000 for C.T.C of particulars of directors (CAC 7), NGN 2,000 for C.T.C of allotment of share capital (CAC 2) and NGN 2,000 for C.T.C of particulars of the company secretary. </p>
        <p>Lawyers are usually hired to undertake the administrative incorporation procedures. On average, a lawyer will charge NGN 50,000 to handle the entire incorporation process in this state.</p>

        <ul>
            <li><strong>Make a company seal</strong></li>
        </ul>

        <p>Section 74 of the Companies and Allied Matters Act (CAMA) requires every company to have a common seal. The company is thus mandated to have a company seal by the Companies and Allied Matters Act 1990 (2004) first schedule paragraph 15, which provides a standard memorandum of articles of companies and requires directors to provide a company seal. It is also the common practice, especially because companies will generally have to register a deed or other official documents.</p>

        <ul>
            <li>
                <strong>Register for income tax and VAT at the Federal Inland Revenue Service </strong>
            </li>
        </ul>

        <p>
            Entrepreneurs can register for both corporate income tax and VAT at the new Integrated Tax Office. For corporate income tax, the Federal Inland Revenue Service (FIRS) will require the applicant to complete tax registration forms. The new company registers at a federal tax office nearest to its place of business or registered office. A letter is written to the tax authority applying for a tax clearance certificate. A form obtained from the tax office is filled and the following documents submitted with it:
            <ol type="a">
                <li>Taxpayer registration input form (TRIF/2006/001 COYS);</li>
                <li>Completed FIRS questionnaire;</li>
                <li>Copy of memorandum and articles of association;</li>
                <li>Names and addresses of directors; </li>
                <li>Copy of the certificate of incorporation; </li>
                <li>Name, address of tax adviser; </li>
                <li>Letter of appointment of tax adviser and a letter of acceptance;</li>
                <li>The date the company commenced business;</li>
                <li>Names, addresses and mobile numbers of major promoters and the chairman of the company, including their email addresses;</li>
                <li>Other sources of income of the chairman and the promoters of the company;</li>
                <li>Name and addresses of the principal officers of the company including the chairman, managing director, legal adviser and accountant. </li>
            </ol>
        </p>

        <p>
            The application forms must be in triplicate and the original certificate of incorporation must be presented for 'sighting' by the controller before registration can be completed. Upon receipt of the completed taxpayer registration input form and all documents, a tax reference number is allocated. An application must be filed for the tax clearance certificate. Its issuance is not automatic. 
        </p>
        <p>
            The fees for the tax clearance issuance are as follows: for newly registered companies within 6 months of incorporation: no fee; for companies that are yet to commence business and show up for registration after 6 months of incorporation, if they request for tax clearance certificate a pre-operation levy of NGN 25,000 is payable for the first time and NGN 20,000 is payable for subsequent years until it files a notice of commencement of business.
        </p>

        <p>
            Companies required to register for VAT complete the VAT registration form (VAT Form 001, obtainable free of charge from all FIRS offices) and return it to the integrated tax office, which will issue a taxpayer identification number (TIN). Companies required to register for VAT must do so within 6 months from the date of company incorporation. 
        </p>

        <p>
            Since the registration for corporate income tax and VAT are done in the same place; one Tax Identification Number (TIN) is issued to companies for all federal taxes.
        </p>
        <ul>
            <li><strong>Register for personal income tax PAYE at the State Tax Office</strong></li>
        </ul>
        <p>
            The employers in the newly incorporated company are required to register with the relevant state tax authority for the purposes of deducting income tax from employees. Once an application is filed with a copy of the certificate of incorporation attached, a reference file is then opened for the company. There is no cost related to this registration.
        </p>

        <ul>
            <li>
                <strong>Receive an inspection from the State Ministry of Commerce and Industry for business premises registration </strong>
            </li>
        </ul>
        <p>
            After the submission of all application documents, an inspection of the business premises is carried out by an officer from the State Ministry of Commerce and Industry. The Ministry may conduct an inspection of the premises to confirm the location of the business and determine the type of business activity.
        </p>
        <ul>
            <li><strong>Pay registration fees for business premises into a government account at a designated bank</strong></li>
        </ul>

        <p>
            The payment of the business premises registration fees is made directly into the government bank account at a designated bank. NGN 10,000 is the cost of business premises registration for a medium size company in an urban area.
        </p>

        <ul>
            <li><strong>Obtain a business premises permit from the State Ministry of Commerce and Industry </strong></li>
        </ul>

        <p>
            The newly incorporated company is required to register its business premises with the State Ministry of Commerce and Industry.
        </p>
