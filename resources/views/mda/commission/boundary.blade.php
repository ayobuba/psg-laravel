      <ol type="i">
        <li>23th October 2014 - meeting held at the Bassa Local Government Secretariat to resolve the boundary problems between Irigwe and Rukuba.</li>
        <li>30th October 2014 – Another meeting was held with the same stakeholders as in (i) above to further find and amicable way of resolving the boundary problems. </li>
        <li>4th November 2014 – Similar meeting held at Bassa Local Government Secretariat with stakeholders on same issue. </li>
        <li>12th November 2014 –Meeting held at Shendam LGC in respect of Shendam and Mikang LGAs over the ownership of Zomo (Suuklut).</li>
        <li>13th November 2014 – Another meeting was held at the LGC Secretariat to find a lasting solution to the lingering boundary problems between Irigwe and Rukuba communities.</li>
        <li>26th November 2014 – A meeting with the two communities and other stakeholders took place at the LGC Secretariat where far-reaching decisions were made.</li>
        <li>17th December 2014 – A meeting was held at the Bassa LGC Secretariat where a Technical Committee cutting across the two communities – Irigwe and Rukuba, the LGC and the Commission was constituted and to commence work on the 8th January 2015.</li>
      </ol>
