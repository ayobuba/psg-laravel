        <h4>Plateau State Afforestation Programme</h4>
        <p>
            The Plateau State Afforestation Programme was established through edict PS.L.N No. 15 of September 1986 to implement loan nos. 2760 UNI, under the World Bank forestry II rural project. It is a community people oriented rural forestry programme, which took off in December 1987, while the final draw down on loan came to a close in June 1996. However, at project implementation completion report meeting with the World Bank, Plateau State Government agreed to sustain the activities of the programme through adequate funding in order to avail beneficiaries project services, but sustenance has been erratic.
        </p>

        <h4>MANDATE AND RESPONSIBILITIES STIPULATED BY LAW </h4>
        <p>
            The mandate and responsibilities of the Plateau State Affforestation Project as stipulated by law include: 
            <ol>                
                <li>To reduce soil degradation and stabilize soil conditions. </li>
                <li>To increase the supply of fuel wood, poles and fodder. </li>
                <li>To involve more individual farmers and encourage them to take up plant more trees and maintain their own seedlings. </li>                    
            </ol>

            These objectives were to be achieved through implementation of the following components:

            <ol type="a">
                <li>Nursery production. </li>
                <li>Upgrading of existing Nurseries. </li>
                <li>Establishment of 8 new nurseries. </li>
                <li>Production of 12.7m seedlings for a life span of 8 years. </li>
                <li>Continue Nursery production by the State Government after World Bank draw down, through sustainability</li>
            </ol>
        </p>
        <h4>ACHIEVEMENTS</h4>

        <p>
            Not withstanding the problems encountered by the project units activities were pursed vigorously as can be seen through the following successes: 

            <ol>
                <li>In- housing capacity building by the extension and training department recorded high achievement. Skills of staff were upgraded in the overseas and local training to cope with the task of project implementation. </li>

                <li>Nursery upgrading and establishment recorded high achievement above target to reduce distribution distances and consequently improving seedlings survival rates. </li>

                <li>Extension linkages with the PADP were successfully implemented under the Unified Agricultural Extension System (UAES). </li>

                <li>Communities were mobilized and supported in Nursery development. 186 communities Nurseries (83%) were established against a project target of 226 Nurseries.</li>

                <li>Institutions and NGOs' were encouraged to establish their own Nurseries, 89% achievement was recorded with the establishment of 73 Nurseries against a project target of 82 nurseries. </li>

                <li>Over all seedlings production from the central and community based nurseries was 12.6m (99.6%) against a target of 12.7m. </li>

                <li>The project unit evolved a system of transporting seedlings to beneficiaries at their doorstep, through village extensions agents (VEAs), were 10.3m (97%) seedlings were produced from the central Nurseries. They were distributed for planting to beneficiaries in the farm forestry Programme. </li>

                <li>Woodlots were established in 1967 planting sites covering an area of 550 hectares of land. </li>

                <li>Women were mobilized and organized into groups where woodlots establishment were encouraged to solve their fuel wood needs. </li>

                <li>Schools were mobilized, where students established their Nurseries where seedlings were planted in their school amenities, boundary plantings and windbreak. Young farmers club were formed where students were engaged to propagate the habit of tree planting, protection and conservation. </li>

                <li>Farmersâ€™ merit Awards was instituted on an annual base to reward farmers, communities, NGOs, forestry conservation clubs and women groups who excelled in woodlot establishment in project, nursery operations and active participation in project activities. </li>

                <li>Farmers were sensitized through radio jingles and T.V announcement, village extension agents, district and village level Afforestation committee on tree production, planting and protection. </li>
            </ol>
        </p>

