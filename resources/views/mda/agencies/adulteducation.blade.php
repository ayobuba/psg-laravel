        <ol>
            <li>To develop policies and strategies aimed at eradicating illiteracy in the State.</li>
            <li>Monitor and standardize the implementation of Adult and Non-Formal Education programmes in the State.</li>
            <li>Network with Local Government Councils, Non-Governmental Organizations (NGOs) and individuals stakeholders, involved in literacy delivery in the State.</li>
