        <h4>OBJECTIVES</h4>
        <p>
            The main objectives for which the Directorate was established are as follows:-
            <ul>
                <li>To lay down policies for Adult and Non-Formal Education towards eradication of illiteracy for the overall social and economic development of the State.</li>
                <li>To provide avenues for Continuing and Vocational Education programmes to people outside the formal school system; particularly those that are relevant to rural development.</li>
                <li>To Forster positive attitudinal changes among people for a sound and comprehensive development.</li>
            </ul>

        </p>
        
