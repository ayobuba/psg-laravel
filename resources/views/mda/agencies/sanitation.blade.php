    <h4>
      PRUWASSA STRUCTURE
    </h4>

    <p>
      At present the Agency operates the following departments; 
      <ul>
        <li>Community Mobilization: the department is responsible for mobilizing communities for the implementation of all our programmes. </li>
        <li>Sanitation and Hygiene Promotion: the department is responsible for the provision of sanitary platform latrine for households, Ventilated Improved Pit latrine (VIP) for schools and markets in selected communities, it also propagates Hygiene Education with particular emphasis on personal and environmental health practices </li>
        <li>Planning Monitoring and Evaluation: the department is responsible for the collection and analysts of relevant data for planning purposes, monitoring and evaluation of our performance </li>
        <li>Water Supply: the department is responsible for conducting Geophysical . Survey, Drilling of Boreholes and Digging of Hand Dug Wells (HDW). It Is also responsible for pump test, Hand pump installation and Water Quality testing of Boreholes </li>
        <li> Administration and Finance: the department is responsible for the day to day running of the Agency </li>

        <li> Workshop: although a new department it is in charge of mechanical repair of light and heavy-duty vehicles. </li>
      </ul>
    </p>

