    <h4>The Mandates of the Agency are as follows.</h4>

    <ol type="i">
      <li>Formulate policies on all activities relating to disaster management in the state.</li>
      <li>Coordinate plans and programs for efficient and effective response to disasters in the state </li>
      <li>Coordinate and promote research activities relating to disaster management in the state.</li>
      <li>Monitor and provide feedback to the National Emergency Management Agency (NEMA) on the state of preparedness of all Organizations/Agencies which may contribute to disaster management within the state </li>
      <li>Collate data and report from relevant Agencies in the state so as to enhance forecasting, planning and field operation of disasters management and supply same to NEMA</li>

      <li>Educate the general public on disasters prevention and control measures for search and a rescue operation. </li>
      <li>Mobilize support and resources from the National Emergency Management Agency when damages and need assessment are considered beyond the capacity of the state</li>
      <li>Facilitate the enabling legislation for the establishment of Local Management Authority (LEMA) in the state etc</li>
    </ol>

